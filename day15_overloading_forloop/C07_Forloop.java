package day15_overloading_forloop;

public class C07_Forloop {

    //100 den 1 e kadar sınırlar dahil 8 ile bşlünebilen yazdırın

    public static void main(String[] args) {

        for (int i = 100; i >=1 ; i--) {

            if (i % 8 == 0) ;
            System.out.println(i + " ");
        }
        }

    }

